		{
			get
			{
				return _RemainingPills;
			}
			set
			{
				if (_RemainingPills != value)
				{
					_RemainingPills = value;
					DoPropertyChanged("RemainingPills");
				}
			}
		}

		public UInt16 get(int x, int y)
		{
			return (UInt16)(laby[y][x] & 7);
		}

		public UInt16 this[int x, int y]
		{
			get
			{
