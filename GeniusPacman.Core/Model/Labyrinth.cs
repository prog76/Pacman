		private Labyrinth(int num)
		{
			this.num = num;
			laby = new UInt16[HEIGHT][];
			chars = new TChars(this);

			RemainingPills = pastilles[num];
			for (int i = 0; i < HEIGHT; i++)
			{
				laby[i] = new UInt16[WIDTH];
				for (int j = 0; j < WIDTH; j++)
				{
					laby[i][j] = labys[num][i][j];
				}
			}
		}

